package productLine;

public class ProductLine {

	private String productLineName;
	private String productLineShortTerm;
	private String productLineMidTerm;
	private String productLineLongTerm;
	private int productLineNumberOfStaff;


	public int getProductLineNumberOfStaff() {
		return productLineNumberOfStaff;
	}

	public void setProductLineNumberOfStaff(int productLineNumberOfStaff) {
		this.productLineNumberOfStaff = productLineNumberOfStaff;
	}

	public String getProductLineShortTerm() {
		return productLineShortTerm;
	}

	public void setProductLineShortTerm(String productLineShortTerm) {
		this.productLineShortTerm = productLineShortTerm;
	}

	public String getProductLineMidTerm() {
		return productLineMidTerm;
	}

	public void setProductLineMidTerm(String productLineMidTerm) {
		this.productLineMidTerm = productLineMidTerm;
	}

	public String getProductLineLongTerm() {
		return productLineLongTerm;
	}

	public void setProductLineLongTerm(String productLineLongTerm) {
		this.productLineLongTerm = productLineLongTerm;
	}

	public String getProductLineName() {
		return productLineName;
	}

	public void setProductLineName(String productLineName) {
		this.productLineName = productLineName;
	}
}
